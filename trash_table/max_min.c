#include "trash_table.h"

/**
 * max - get the greater of two values
 * @a: value 1
 * @b: value 2
 *
 * Return: the greater value
 */
size_t max(size_t a, size_t b)
{
	return (a > b ? a : b);
}

/**
 * min - get the lesser of two values
 * @a: value 1
 * @b: value 2
 *
 * Return: the lesser value
 */
size_t min(size_t a, size_t b)
{
	return (a < b ? a : b);
}
